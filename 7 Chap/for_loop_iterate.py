#for loops with list
l = [0,1,2,3]
 
for i in l:
    print(i)
#for loops with tuple
t = (9,8,7,6)
for i in t:
    print(i)
#for loops with dict
d = {"kaif":"khan","rahim":"syed"}
for i in d:
    print(i)
#for loops with sets
s = {"a","b","c","d"}
for i in s:
    print(i)
#for loops with strings
name = "kaihan"
for i in name:
    print(i)
